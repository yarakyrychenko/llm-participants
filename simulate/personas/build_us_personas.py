import json
import os
import re
import sys

# ---------------------------------------------------------------------------
# Census target distributions for stratification (2023 ACS / Pew)
# ---------------------------------------------------------------------------

# Race targets (meta_persona RACE field values from Census)
RACE_TARGETS = {
    "White": 0.595,
    "Hispanic": 0.190,
    "Black": 0.130,
    "Asian": 0.060,
    "Other": 0.025,  # AIAN, NHPI, Two or more races
}

# Age group targets (18+, US Census 2023)
AGE_TARGETS = {
    "18-29": 0.220,
    "30-44": 0.250,
    "45-59": 0.240,
    "60+":   0.290,
}

# Sex targets
SEX_TARGETS = {
    "Female": 0.507,
    "Male":   0.493,
}

# US region → states mapping (for region-balanced sampling)
REGION_STATES = {
    "Northeast": ["CT", "ME", "MA", "NH", "NJ", "NY", "PA", "RI", "VT"],
    "Midwest":   ["IL", "IN", "IA", "KS", "MI", "MN", "MO", "NE", "ND", "OH", "SD", "WI"],
    "South":     ["AL", "AR", "DC", "DE", "FL", "GA", "KY", "LA", "MD", "MS", "NC", "OK", "SC", "TN", "TX", "VA", "WV"],
    "West":      ["AK", "AZ", "CA", "CO", "HI", "ID", "MT", "NV", "NM", "OR", "UT", "WA", "WY"],
}
STATE_TO_REGION = {s: r for r, states in REGION_STATES.items() for s in states}

# Model to use for all text tiers
MODEL = "Llama-3.1-70B-Instruct"

# Target number of personas
TARGET_N = 10_000
DEFAULT_OUTPUT_FILENAME = "us_personas.json"
FULL_OUTPUT_FILENAME = "us_personas_full.json"

