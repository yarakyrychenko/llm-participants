def _finalize_personas(selected):
    final_personas = []
    for i, p in enumerate(selected, start=1):
        p = {k: v for k, v in p.items() if not k.startswith("_")}
        p["id"] = f"us_{i:05d}"
        final_personas.append(p)
    return final_personas


