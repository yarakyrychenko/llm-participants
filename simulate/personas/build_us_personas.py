def stratify(personas, target_n):
    """
    Stratified sampling by race × sex × age_bucket to match Census targets.
    Returns target_n personas.
    """
    from collections import defaultdict
    import random
    random.seed(42)

    # Build strata
    strata = defaultdict(list)
    for p in personas:
        race_k = p.get("_race_key", "Other")
        sex_k = p.get("_sex_key", "Male")
        age_k = p.get("_age_bucket")
        if age_k is None:
            continue
        key = (race_k, sex_k, age_k)
        strata[key].append(p)

    # Compute target counts per stratum
    result = []
    allocated = 0
    stratum_targets = {}
    for race_k, race_w in RACE_TARGETS.items():
        for sex_k, sex_w in SEX_TARGETS.items():
            for age_k, age_w in AGE_TARGETS.items():
                key = (race_k, sex_k, age_k)
                count = round(target_n * race_w * sex_w * age_w)
                stratum_targets[key] = count

    # Allocate from each stratum
    for key, target_count in sorted(stratum_targets.items()):
        pool = strata.get(key, [])
        if not pool:
            continue
        if len(pool) >= target_count:
            sampled = random.sample(pool, target_count)
        else:
            # Oversample with replacement if we don't have enough
            sampled = pool.copy()
            while len(sampled) < target_count:
                sampled.extend(random.sample(pool, min(len(pool), target_count - len(sampled))))
            sampled = sampled[:target_count]
        result.extend(sampled)

    # Shuffle and trim to exact target
    random.shuffle(result)
    result = result[:target_n]

    return result


