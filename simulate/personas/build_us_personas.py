def build_database(target_n=TARGET_N, full=False):
    try:
        from datasets import load_dataset
    except ImportError:
        print("ERROR: 'datasets' library not found. Install with: pip install datasets")
        sys.exit(1)

    print(f"Loading Tianyi-Lab/Personas from HuggingFace (streaming)...")
    print("This may take a few minutes on first run (caches locally after).")

    ds = load_dataset(
        "Tianyi-Lab/Personas",
        split="train",
        streaming=True,
    )

    raw_personas = []
    skipped = 0
    print("Parsing personas...", flush=True)

    for i, row in enumerate(ds):
        persona = normalize_persona(row)
        if persona is None:
            skipped += 1
            continue
        raw_personas.append(persona)

        if (i + 1) % 5000 == 0:
            print(f"  Processed {i+1} rows, kept {len(raw_personas)} valid personas...", flush=True)

    print(f"Done. Total rows: {i+1}, valid personas: {len(raw_personas)}, skipped (under 18): {skipped}")

    if full:
        print("Building full uncapped adult-US persona database...")
        selected = raw_personas
        sampling_strategy = "full"
    elif len(raw_personas) < target_n:
        print(f"WARNING: Only {len(raw_personas)} valid personas available; using all of them.")
        selected = raw_personas
        sampling_strategy = "full"
    else:
        print(f"Stratifying to {target_n} personas (Census-weighted)...")
        selected = stratify(raw_personas, target_n)
        sampling_strategy = "stratified"

    final_personas = _finalize_personas(selected)

    database = {
        "metadata": _build_metadata(
            final_personas,
            sampling_strategy=sampling_strategy,
            target_n=target_n if sampling_strategy == "stratified" else None,
        ),
        "personas": final_personas,
    }

    return database


def _parse_args():
    parser = argparse.ArgumentParser(description="Build the US persona database from Tianyi-Lab/Personas.")
    parser.add_argument(
        "--full",
        action="store_true",
        help="Build the full uncapped adult-US dataset instead of the 10,000-person stratified sample.",
    )
    parser.add_argument(
        "--target-n",
        type=int,
        default=TARGET_N,
        help="Target size for the stratified sample build. Ignored when --full is set.",
    )
    parser.add_argument(
        "--output",
        type=str,
        default=None,
        help="Optional output filename. Defaults to us_personas.json or us_personas_full.json based on mode.",
    )
    return parser.parse_args()


if __name__ == "__main__":
    args = _parse_args()
    db = build_database(target_n=args.target_n, full=args.full)
    output_filename = args.output or (FULL_OUTPUT_FILENAME if args.full else DEFAULT_OUTPUT_FILENAME)
    out_path = os.path.join(os.path.dirname(__file__), output_filename)
    print(f"Writing {db['metadata']['total_personas']} personas to {out_path}...")
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(db, f, indent=2, ensure_ascii=False)
    print(f"Done. File size: {os.path.getsize(out_path) / 1e6:.1f} MB")
    print()
    print("Distribution:")
    for k, v in db["metadata"]["actual_distributions"].items():
        print(f"  {k}: {dict(sorted(v.items(), key=lambda x: -x[1]))}")
