"""
build_us_personas.py — Build the US persona database from Tianyi-Lab/Personas.

Source: Tianyi-Lab/Personas (arXiv:2503.16527, NeurIPS 2025)
HuggingFace: https://huggingface.co/datasets/Tianyi-Lab/Personas
License: Publicly available (no gating)

The dataset has ~48,000 US personas across 48 states. Each row contains three
tiers generated by 6 LLMs. We use the Llama-3.1-70B-Instruct tier (highest quality)
for all text fields, with meta_persona as ground truth for Census-grounded demographics.

Three tiers per row:
  1. meta_persona (dict): AGE (range), SEX, RACE, STATE — Census joint distribution
  2. objective_table_persona (JSON string): 20+ structured demographic fields
  3. subjective_table_persona (JSON string): personality, political views, religion,
     BIG_FIVE_SCORES, IDEOLOGY, LIFESTYLE, DEFINING_QUIRKS, MANNERISMS, PERSONAL_TIME
  4. descriptive_persona (str): 1,000–5,000 char narrative biography

Outputs:
  - simulate/personas/us_personas.json — 10,000 stratified personas
  - simulate/personas/us_personas_full.json — full uncapped adult-US database
Fields compatible with format_personalization() [TAG] system in simulate/agent.py.

Run:
    pip install datasets
    python3 -m simulate.personas.build_us_personas
    python3 -m simulate.personas.build_us_personas --full
"""

