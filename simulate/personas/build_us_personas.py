def safe_parse_json(raw):
    """Parse a JSON string; return empty dict on failure."""
    if isinstance(raw, dict):
        return raw
    if not isinstance(raw, str):
        return {}
    raw = raw.strip()
    # Strip surrounding quotes if double-encoded
    if raw.startswith('"') and raw.endswith('"'):
        try:
            raw = json.loads(raw)
        except Exception:
            pass
    try:
        return json.loads(raw)
    except Exception:
        return {}


def map_age_range(age_str):
    """Convert Census age range string to integer midpoint."""
    if not age_str:
        return None
    # Try direct int
    try:
        return int(str(age_str))
    except (ValueError, TypeError):
        pass
    # Try range like "75-84"
    m = re.match(r'(\d+)-(\d+)', str(age_str))
    if m:
        lo, hi = int(m.group(1)), int(m.group(2))
        return (lo + hi) // 2
    # Try "85+" or "65+"
    m = re.match(r'(\d+)\+', str(age_str))
    if m:
        return int(m.group(1)) + 5
    return None


def get_age_bucket(age):
    if age is None:
        return None
    if age < 18:
        return None
    if age < 30:
        return "18-29"
    if age < 45:
        return "30-44"
    if age < 60:
        return "45-59"
    return "60+"


def normalize_race(race_str):
    """Map Census race/ethnicity labels to clean standard values."""
    if not race_str:
        return "Other"
    r = str(race_str).strip()
    mapping = {
        "White": "White",
        "White alone": "White",
        "Black": "Black",
        "Black or African American": "Black",
        "Black alone": "Black",
        "Hispanic": "Hispanic",
        "Hispanic or Latino": "Hispanic",
        "Asian": "Asian",
        "Asian alone": "Asian",
        "American Indian": "Other",
        "Alaska Native": "Other",
        "Native Hawaiian": "Other",
        "Pacific Islander": "Other",
        "Two or More Races": "Other",
        "Some Other Race": "Other",
        "Other": "Other",
    }
    for key, val in mapping.items():
        if r.lower() == key.lower() or r.lower().startswith(key.lower()):
            return val
    # Check for Hispanic via regex
    if re.search(r'hispanic|latino', r, re.IGNORECASE):
        return "Hispanic"
    return r  # Return as-is if no match


def normalize_sex(sex_str):
    if not sex_str:
        return "Unknown"
    s = str(sex_str).strip().lower()
    if s in ("female", "f", "woman"):
        return "Woman"
    if s in ("male", "m", "man"):
        return "Man"
    return str(sex_str).strip()


def extract_name_from_bio(bio_str):
    """Try to extract the persona's name from the narrative bio."""
    if not bio_str:
        return None
    # Common patterns: "Meet Sarah Johnson," or "John Smith is a 45-year-old"
    patterns = [
        r'^(?:Meet\s+)?([A-Z][a-z]+(?:\s+[A-Z][a-z]+){1,3})[,\s]',
        r'^(?:Name:\s*\*\*?)([A-Z][a-z]+(?:\s+[A-Z][a-z]+){1,3})(?:\*\*?)?',
        r'\*\*Name:\*\*\s*([A-Z][a-z]+(?:\s+[A-Z][a-z]+){1,3})',
    ]
    for pat in patterns:
        m = re.search(pat, bio_str.strip())
        if m:
            name = m.group(1).strip()
            # Filter out generic openers
            if name.lower() not in ("meet", "this", "the", "a", "an"):
                return name
    return None


def parse_big_five(bf_val):
    """Parse BIG_FIVE_SCORES field to a readable string."""
    if not bf_val:
        return None
    if isinstance(bf_val, dict):
        parts = []
        for trait, score in bf_val.items():
            trait_short = trait.replace("_", " ").replace("Big Five: ", "").strip()
            parts.append(f"{trait_short}: {score}")
        return "; ".join(parts)
    return str(bf_val)[:300]


def normalize_persona(row):
    """
    Parse a Tianyi-Lab/Personas row into a normalized persona dict.
    Returns None if the persona is under 18 or missing critical fields.
    """
    # --- Census-grounded meta fields ---
    meta = row.get("meta_persona", {})
    if isinstance(meta, str):
        meta = safe_parse_json(meta)

    raw_age = meta.get("AGE") or meta.get("Age")
    raw_sex = meta.get("SEX") or meta.get("Sex")
    raw_race = meta.get("RACE") or meta.get("Race")
    raw_state = str(meta.get("STATE") or meta.get("State") or row.get("state", "")).strip().upper()

    # Age check
    age = map_age_range(raw_age)
    if age is None or age < 18:
        return None

    sex = normalize_sex(raw_sex)
    race = normalize_race(raw_race)
    state = raw_state or "Unknown"
    region = STATE_TO_REGION.get(state, "Unknown")

    # --- Objective table (detailed demographics) ---
    obj_key = f"{MODEL}_objective_table_persona"
    obj = safe_parse_json(row.get(obj_key, "{}"))

    education = obj.get("EDUCATION") or obj.get("Education") or ""
    employment_status = obj.get("EMPLOYMENT_STATUS") or obj.get("Employment Status") or ""
    income_raw = obj.get("INCOME") or obj.get("Income") or ""
    marital_status = obj.get("MARITAL_STATUS") or obj.get("Marital Status") or obj.get("MARITAL STATUS") or ""
    ancestry = obj.get("ANCESTRY") or obj.get("Ancestry") or ""
    household_language = obj.get("HOUSEHOLD_LANGUAGE") or obj.get("Household Language") or "English"
    citizenship = obj.get("CITIZENSHIP") or obj.get("Citizenship") or ""
    veteran_status = obj.get("VETERAN_STATUS") or obj.get("Veteran Status") or ""
    health_insurance = obj.get("HEALTH_INSURANCE") or obj.get("Health Insurance") or ""
    job_description = obj.get("DETAILED_JOB_DESCRIPTION") or obj.get("Detailed Job Description") or ""

    # Clean income string
    income_str = str(income_raw).replace(",", "").replace("$", "").strip()
    try:
        income_val = int(re.sub(r'[^\d]', '', income_str))
        # Categorize relative to US median (~$60k)
        if income_val < 35000:
            income_level = "Low"
        elif income_val < 90000:
            income_level = "Middle"
        else:
            income_level = "High"
    except (ValueError, TypeError):
        income_level = income_str if income_str else "Unknown"

    # --- Subjective table (personality, political views, religion) ---
    subj_key = f"{MODEL}_subjective_table_persona"
    subj = safe_parse_json(row.get(subj_key, "{}"))

    ideology = subj.get("IDEOLOGY") or subj.get("Ideology") or ""
    political_views = subj.get("POLITICAL_VIEWS") or subj.get("Political Views") or subj.get("POLITICAL VIEWS") or ""
    religion = subj.get("RELIGION") or subj.get("Religion") or ""
    lifestyle = subj.get("LIFESTYLE") or subj.get("Lifestyle") or ""
    defining_quirks = subj.get("DEFINING_QUIRKS") or subj.get("Defining Quirks") or ""
    mannerisms = subj.get("MANNERISMS") or subj.get("Mannerisms") or ""
    personal_time = subj.get("PERSONAL_TIME") or subj.get("Personal Time") or ""
    big_five_raw = subj.get("BIG_FIVE_SCORES") or subj.get("Big Five Scores") or subj.get("BIG FIVE SCORES")
    big_five = parse_big_five(big_five_raw)

    # Map ideology to standardized political_orientation
    ideology_lower = str(ideology).lower()
    if any(x in ideology_lower for x in ["very liberal", "far left", "progressive", "socialist"]):
        political_orientation = "Very liberal"
    elif any(x in ideology_lower for x in ["liberal", "left-leaning", "left of center"]):
        political_orientation = "Liberal"
    elif any(x in ideology_lower for x in ["very conservative", "far right", "hard right"]):
        political_orientation = "Very conservative"
    elif any(x in ideology_lower for x in ["conservative", "right-leaning", "right of center"]):
        political_orientation = "Conservative"
    elif any(x in ideology_lower for x in ["moderate", "centrist", "independent", "center"]):
        political_orientation = "Moderate"
    else:
        political_orientation = ideology or "Unknown"

    # Map ideology to party (rough heuristic for US context)
    party = "Independent"
    if any(x in ideology_lower for x in ["liberal", "progressive", "democrat"]):
        party = "Democrat"
    elif any(x in ideology_lower for x in ["conservative", "republican"]):
        party = "Republican"
    elif "libertarian" in ideology_lower:
        party = "Libertarian"

    # --- Descriptive persona (narrative bio) ---
    bio_key = f"{MODEL}_descriptive_persona"
    bio = str(row.get(bio_key, "")).strip()
    # Strip outer quotes if wrapped
    if bio.startswith('"') and bio.endswith('"'):
        try:
            bio = json.loads(bio)
        except Exception:
            bio = bio[1:-1]

    # Extract name from bio
    name = extract_name_from_bio(bio)

    # Build normalized persona
    persona = {
        # IDs and location
        "country": "United States",
        "country_code": "US",
        "state": state,
        "region": region,

        # Core demographics (Census-grounded via meta_persona)
        "age": age,
        "gender": sex,
        "race": race,          # backward-compat alias → [RACE]
        "ethnicity": race,

        # Structured demographics (LLM-generated from Census constraints)
        "education": str(education),
        "employment_status": str(employment_status),
        "income_level": str(income_level),
        "marital_status": str(marital_status),
        "ancestry": str(ancestry),
        "household_language": str(household_language),
        "citizenship": str(citizenship),
        "veteran_status": str(veteran_status),
        "health_insurance": str(health_insurance),
        "job_description": str(job_description),

        # Psychographic / political (subjective table)
        "political_orientation": str(political_orientation),
        "political_party": str(party),
        "party": str(party),   # backward-compat alias → [PARTY]
        "ideology_raw": str(ideology),
        "political_views": str(political_views),
        "religion": str(religion),
        "lifestyle": str(lifestyle),
        "hobbies": str(personal_time),
        "defining_quirks": str(defining_quirks),
        "mannerisms": str(mannerisms),
        "big_five": str(big_five) if big_five else "",

        # Open-text biography (descriptive tier)
        "bio": bio,
        "name": name or "",

        # Stratification keys (for internal use)
        "_age_bucket": get_age_bucket(age),
        "_race_key": normalize_race(race) if normalize_race(race) in RACE_TARGETS else "Other",
        "_sex_key": "Female" if sex == "Woman" else "Male",
    }

    return persona


